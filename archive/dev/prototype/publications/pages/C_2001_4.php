		<h3>Sensitivity of electron-hole integrals to choice of atomic orbitals in tight-binding models for nanocrystals.</h3>
		
		<p>Lars J&ouml;nsson, Seungwon Lee, John W. Wilkins, Garnett W. Bryant, and Gerhard Klimeck,  </p>
		
		
		<p>March Meeting of the American Physical Society, March 12-16, Seattle, WA (2001).</p>
		
		<h3>Abstract:</h3> 
		<p>Empirical tight-binding (TB) models give computationally efficient and accurate atomistic descriptions of
quantum dots for a broad range of materials and structures. The matrix elements of the TB Hamiltonian are
fitted to the bulk band structure without an explicit choice for the atomic orbitals. However, to calculate
Coulomb integrals between electrons and holes a specific choice is needed. We compare the use of
Gaussian-type and Slater orbitals, with both orthogonal and nonorthogonal sp^3 hybrids, for Coulomb and
exchange integrals in Si, InAs, and CdSe spherical nanocrystals. Most onsite and nearest-neighbor (NN)
integrals vary by 20--50% between different orbital choices. However, for orthogonal hybrids, NN exchange
integrals are negligible, while without orhogonalization they can be a factor of 30 too large. As the dot size
increases, the electron-hole integrals become less sensitive to the choice of orbitals. For dots with radius
above 15-20 ¬Åthe Coulomb integrals vary by less than 10% despite the 20--50% variations in the onsite and
NN integrals.  </p>

				
		
		
