		<h2>Gerhard Klimeck J_2002_5 </h2>
		<h3>Parallelization of the Nanoelectronic Modeling Tool (NEMO 1-D) on a Beowulf Cluster</h3>
		<p>Gerhard Klimeck </p>
		<p>Accepted for publication in the Journal of Computational Electronics, 2002,</p>
		<h3>Abstract:</h3> 
		<p>NEMO's main task is the computation of current-voltage (I-V) characteristics for resonant tunneling diodes (RTDs). The primary model for high performance RTDs is the full band sp3s* tight binding simulation, which is based on a numerical double integral of energy and transverse momentum over a transport kernel at each bias point.  A full charge self-consistent simulation invoking this model on a single CPU is prohibitively expensive, as the generation of a single I-V curve would take about 1-2 weeks to compute.  Simplified charge self-consistent models, eliminating the numerical momentum integral for the quantum mechanical charge self-consistency, followed by a single pass double integration for the current, have been used in the past.  However, Computation on a parallel computer now enables the thorough exploration of quantum mechanical transport including charge self-consistency effects within the entire Brillouin zone based on the double integral.  Various parallelization schemes (fine, coarse, and mixed) are presented and evaluated in their performance.  Finally a comparison to experimental data is given. </p>
		<p>Article <a href="../pubs_src/J40.pdf">PDF</a>.</p>
