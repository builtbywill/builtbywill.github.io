		<h2>Gerhard Klimeck J04  </h2>
				
		
		<h3>&quot;Rate Equations from the Keldysh Formalism Applied to the Phonon Peak in Resonant-Tunneling Diodes&quot;</h3>
		
		<p>Roger Lake, Gerhard Klimeck, and Supriyo Datta</p>
		
		<p>Phys. Rev. B, Vol. 47, p.6427 (1993)</p>
		
		<h3>Abstract:</h3> 
        <p>Starting from the Keldysh formalism, general analytical expressions are derived for the current and the occupation of the well in the presence of inelastic scattering, both at the main peak and at the phonon peak. These expressions are then evaluated from a continuous coordinate representation of a double-barrier potential profile and also from a tight-binding model of a weakly coupled central site. The resulting expressions are similar, and the analytical expressions derived from the continuous coordinate representation compare well with the results obtained from numerical simulations. The analytical expressions and the numerical results show that unlike the main peak, the phonon peak is normally independent of the collector transmissivity. But with very opaque collector barriers, the resonant level fills up and the current decreases because the inelastic scattering is suppressed by the exclusion principle. An alternative but equivalent point of view is that the effective coupling GAMMA(E)' between the incident energy in the emitter and the resonant energy in the well is g'GAMMA(E) Where g' is the effective phonon coupling constant and GAMMA(E) is HBAR times the tunneling rate through the emitter barrier. The total low-temperature inelastic current at the phonon-peak bias is (2e/HBAR)GAMMA(E)'GAMMA(C)/(GAMMA(E)' + GAMMA(C)). Since g' &lt; 1, the effective coupling GAMMA(E)' determines the current until GAMMA(C) is reduced to approximately GAMMA(E)'. The ''backflow'' correction to the current due to absorption of phonons is derived, interpreted, and limiting cases discussed. The approach described here could be applied to other problems involving resonant tunneling in the presence of inelastic scattering.</p>
		
