		<h3>Tight binding sp3s* material parameters from genetic algorithms</h3>
		
		<p>Gerhard Klimeck, R. Chris Bowen, Tom Cwik, and Timothy B. Boykin</p>
		
		<p>American Physical Society Centennial Meeting, March 22-26, Atlanta, GA.</p>
		
		<h3>Abstract:</h3> 
        <p>Tight binding sp3s* bandstructure models have proven to be essential for the quantitative modeling of electron transport in room temperature resonant tunneling diodes. The key ingredients to the success of the models are the abilities to 1) discretize heterostructures interfaces on an atomic scale and 2) include band-nonparabolicity, band warping, and multiple valleys on equal footing. The versatility of the model comes at the price of 15 and 37 non-intuitive orbital interaction energy parameters for every different compound in the nearest and second nearest neighbor model. While equations for effective masses at <font face="Symbol">G</font> as a function of these interaction energies are now available the particular choice to match the bulk bandstructure of a particular compound remaines tedious at best. To address this problem a genetic algorithm based optimization procedure was developed for operation in a 15 or 37 dimensional space. Matches to experimental observables such as effective masses at <font face="Symbol">G</font>, X, and L can be traded off by choice of weights in the fitness function. This automation of material parameter generation enables the straight forward inclusion of new materials into NEMO.</p>
		
		
		
