		<h3>&quot;Rate Equations for the Phonon Peak in Resonant Tunneling Structures&quot;</h3>
		
		<p>Roger Lake, Gerhard Klimeck, M. P. Anantram, and Supriyo Datta</p>
		
		<p>Phys. Rev. B., Vol. 48, p.15132 (1993).</p>
		
		<h3>Abstract:</h3> <p>The ratio of the phonon peak current to the main peak current in double-barrier resonant-tunneling structures is significantly enhanced by barrier asymmetry. Previously, using the Keldysh formalism, we derived analytical expressions, valid in the zero-temperature, high-bias regime, which explained this effect. We now provide analytical expressions valid for finite temperature and bias obtained from (i) an intuitive derivation using a rate equation approach and (ii) a more general derivation using the Keldysh formalism. The results of the two different approaches are shown to be essentially identical for the experimental device parameters. The finite temperature expressions shed light on the effect of the Pauli exclusion factors in the contacts on the current. In particular, we show that in a transmission formulation, the transmission coefficients, T(epsilon,epsilon'), are themselves functions of the Fermi factors in the contacts.</p>
		
