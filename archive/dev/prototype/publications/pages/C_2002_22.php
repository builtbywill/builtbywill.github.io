		<h3>Transport simulation of precessing spin distribution across semiconductor heterojunctions		</h3>
        <p>Paul von Allmen and Gerhard Klimeck,</p>
		<p>Materials Research Society Meeting, Boston, MA, Dec 1-6, 2002.	</p>
        <h3>Abstract:</h3> 
        <p>Magneto-optic experiments have recently investigated the transport of precessing spin distributions across a semiconductor heterojunction. It was found that a spin polarized electron distribution efficiently diffuses from a GaAs substrate to a ZnSe epilayer [1]. Time resolved Faraday rotation measurements have shown that the dynamics of electrons accumulated in the epilayer is determined by the ZnSe Land\&#146;{e} factor and spin relaxation time. Later measurements have shown that the spin dynamics changes from ZnSe to GaAs in nature if a potential drop is applied between the epilayer and the substrate [2]. We will present numerical results that reproduce these experimental data and show that the shift in spin dynamics is determined by the relative time scales of spin diffusion and Larmor precession. Two approaches will be examined. In the first approach, we solve the continuity equation for spin accumulation as was used for the study of the spin momentum transfer mechanism [3]. The second model relies on the non-equilibrium Green&#146;s function formalism to describe the coupled dynamics of spin diffusion and electron thermalization. Both approaches lead to numerical results in excellent agreement with experiments.</p>
		
