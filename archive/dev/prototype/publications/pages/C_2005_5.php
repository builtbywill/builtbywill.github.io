		<h2>Gerhard Klimeck C_2005_5 </h2>
				
		
		<h3>Effect of anharmonicity of the inter-atomic potential on the built-in strain in epitaxial quantum dot structures</h3>
		
		<p>Olga L. Lazarenkova, S. Lee, P. von Allmen, F. Oyafuso, G. Klimeck, M. Korkusinski, R. Timm, H. Eisele, M. Dahne</p>
		
		
		
		<p>APS March Meeting, Los Angeles, CA, March 21-25, 2005</p>
		
		<h3>Abstract:</h3>
		
		<p>It is demonstrated that the anharmonicity of the inter-atomic potential is important in covered nanostructures. Compared to the strain distribution found with the standard Keating model, corrections of over 100 meV are found in electronic band offsets, resulting in values significantly closer to the experimental data. The anharmonicity correction coefficients for Si, Ge, AlAs, GaAs, InAs, AlSb, GaSb, and InSb are presented. The simulated lattice constant profiles and deformation of the cleaved surface are shown to be in a good agreement with the data observed by XSTM measurements for InAs/GaAs and GaSb/GaAs quantum dot structures. The anharmonicity corrections can be performed without a significant increase of the computational cost, since the model remains limited to the nearest neighbor interactions. Simulations of strained systems containing up to 30 million atoms are demonstrated. This work was performed while OLL held a National Research Council Research Associateship Award at Jet Propulsion Laboratory. </p>
		
		
		
		
		
		
