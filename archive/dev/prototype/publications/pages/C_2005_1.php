		<h2>Gerhard Klimeck C_2005_1 </h2>
				
		<h3>Valley splitting in low-density quantum-confined heterostructures:  Superposition, not Spin!</h3>
		
		<p>Timothy B. Boykin, Gerhard Klimeck, S. N. Coppersmith, Mark Friesen, Paul von Allmen, Seungwon Lee, and Fabiano Oyafuso</p>
		
		<p>APS March Meeting, Los Angeles, CA, March 21-25, 2005</p>
		
		
		<h3>Abstract:</h3>  
		<p>Although valley splitting in Si quantum-confined heterostructures has been studied for 
			many years, it is far less well understood than one might expect.  Because valley 
			degeneracy is problematic in spin quantum computing as a potential source of 
			decoherence and other difficulties it is essential that its origins be thoroughly explained.  
			We explain the valley splitting in Si quantum wells using a simple tight-binding model 
			which eliminates the artificial valley coupling constants found in multiband/multi-valley 
			effective mass treatments.  The results of the simple tight-binding model agree well with 
			multiband tight-binding results, and explain the changing parity of the ground state, and 
			the behavior of the splitting as a function of well width.  The results show that the valley 
			splitting has nothing to do with spin, but is instead purely due to the superposition of 
			states in the quantum well.</p>
		
		
		
		
		
