		<h2>Gerhard Klimeck C_2000_2  </h2>
				
		
		<h3>A Prototype of a 3-D Nanoelectronic Modeling Tool (NEMO-3D)</h3>
		
		<p>Gerhard Klimeck, R. Chris Bowen, Tom Cwik, and Timothy B. Boykin</p>
		
		<p>Nanospace 2000, Houston, TX Jan. 23-28, 2000.</p>
		
		<p>The goal to reduce payload in future space missions while increasing mission capability demands miniaturization of measurement, analytical, and communication systems. The enabling technology is the miniaturization of semiconductor devices of the past 40 years. It has now become evident that the atomic dimensions of the underlying crystalline lattice and the countable electron number ultimately limit the scaling in semiconductor devices. Structures and devices that are being built on the nanometer scale typically cannot be characterized non-invasively without destroying the surrounding structure. Physics-based simulation of such nano-scaled devices can serve as a characterization tool and, by providing feedback to experimental parameters, as a design tool. However, a general-purpose physics-based 3-D quantum device simulation tool does not exist up-to-date.
		Raytheon/TI developed a 1-D quantum device simulator (NEMO-1D). In that effort expertise in device physics, numerical and graphical user interface technology were combined to produce the first quantitative, general-purpose quantum device simulator. The work presented here presents an extension of the of the NEMO software to 3-D modeling and high performance computing. We will discuss the various facets of or NEMO-3D prototype: bandstructure models, genetic algorithm-based parameter exploration, parallel high performance eigenvalue solvers, graphical user interfaces.
		Within the JPL mission we first pursue the simulation of optical absorption in self-assembled InAs/GaAs quantum dots. Issues to be addressed are the effects of size and composition variations as well as the effect of alloy formation at the InAs/GaAs interfaces.</p>
		
		
		
