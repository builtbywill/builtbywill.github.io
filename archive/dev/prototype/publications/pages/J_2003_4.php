		<p>Cristian Rivas, Roger Lake, William R. Frensley, Gerhard Klimeck, Phillip E. Thompson, Karl D. Hobart, Sean L. Rommel, and Paul R. Berger,</p>
        <p>Submitted to Appl. Phys. Lett, (2002).</p>
		<h3>Abstract:</h3>
		<p>The current of an low temperature MBE grown Sb and B delta-doped Si tunnel diode is simulated in all regions of tunneling: peak, valley, and post-valley turn-on. The resulting current-voltage (I-V) characteristics are overlaid on the experimental data. All three regions of the I-V are qualitatively captured by the calculations. The inclusion in the model of bandtail states gives rise to the excess current and the post-valley turn on of the tunnel current. The current calculations use a second neighbor sp3s* planar orbital basis within the non-equilibrium Green function formalism.</p>
