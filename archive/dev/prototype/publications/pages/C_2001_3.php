		<h3>A tiger by the tail: the momentum operator in tight-binding</h3>
		
		<p>Timothy B. Boykin, Gerhard Klimeck, and R. Chris Bowen,  </p>
		
		
		<p>March Meeting of the American Physical Society, March 12-16, Seattle, WA (2001).</p>
		
		<h3>Abstract:</h3> 
		<p>The empirical tight-binding method enjoys wide usage due to its ability to reproduce realistic bandstructures
for many semiconductors. Observables other than the bulk bands can be correctly calculated with the
method, but doing so requires that one take care when deriving the tight-binding representations of operators.
This is especially true of the momentum operator and all quantities with which it is associated. We discuss
how this operator affects such seemingly unrelated problems as the proper calculation of effective masses and
the coupling of the vector potential into the tight-binding Hamiltonian, in both cases demonstrating the link
between the tight-binding and more familiar continuous-space expressions. We also discuss the relevance to
quantum-confined structures such as quantum wells and dots. </p>

				
		
		
