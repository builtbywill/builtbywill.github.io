		<h3>&quot;Genetically Engineered&quot; Nanostructure Devices</h3>
		
		<p>Gerhard Klimeck, Carlos H. Salazar-Lazaro, Adrian Stoica, and Tom Cwik</p>
		
		<p>in &quot;Materials in Space Science, Technology, and Exploration&quot;, MRS Symposium Proceedings, Vol. 551, pg. 149 (1999).</p>
		
		<h3>Abstract:</h3> 
        <p>Material variations on an atomic scale enable the quantum mechanical functionality of devices such as resonant tunneling diodes (RTDs), quantum well infrared photodetectors (QWIPs), quantum well lasers, and heterostructure field effect transistors (HFETs). The design and optimization of such heterostructure devices requires a detailed understanding of quantum mechanical electron transport. The Nanoelectronic Modeling Tool (NEMO) is a general-purpose quantum device design and analysis tool that addresses this problem. NEMO was combined with a parallelized genetic algorithm package (PGAPACK) to optimize structural and material parameters. The electron transport simulations presented here are based on a full band simulation, including effects of non-parabolic bands in the longitudinal and transverse directions relative to the electron transport and Hartree charge self-consistency. Two different numerical experiments that maximize the fit between experiment and simulation are presented: 1) structural variations in layer widths and doping concentrations, and 2) variations in the bulk bandstructure parameters.</p>
		
