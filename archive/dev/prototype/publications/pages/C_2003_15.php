		<h3>Nanoelectronic Modeling (NEMO) for Realistic Simulations of Solid-State Quantum Computing Gates		</h3>
		 
		<p>Gerhard Klimeck, Paul von Allmen, Seungwon Lee, Fabiano Oyafuso, Olga Lazarenkova,Timothy B. Boykin</p>
		<p>DARPA Focused Quantum Systems (FoQuS) Workshop, Falls Church, VA, Jan 28-29 (2004).</p>
		
		<h3>Abstract:</h3>
		<p>Almost all realistically scalable qubit devices and circuits rely on a nano-scale semiconductor implementation. &nbsp;No one in today's semiconductor industry can any longer imagine the on-going device development without modeling tools.&nbsp; The problems with empirical device design and characterization only worsen at the nano-scale.&nbsp; Experimental efforts are limited by physical characterization methods in an overwhelmingly vast design space.&nbsp; <i>Modeling is therefore an essential ingredient</i> in the design and characterization of nano-scale qubit devices.&nbsp; <i>However, no such tool exists today</i>.&nbsp; We have begun to fill that void with the development of the comprehensive device design tool NEMO. &nbsp;Based on the investment of approximately 32 work-years, NEMO is the world's state-of-the-art quantum device design and analysis tool.&nbsp; We have recently addresses fundamental issues on quantum dot quantum computing in Si with multi-million atom simulations. Our continued tool development will enable the comprehensive design and characterization of nano-scale qubit implementations that are based on electron-spin and / or electron-photon interactions.&nbsp; The output of our effort will be the delivery of a comprehensive suite of modeling tools essential to narrow the experimental design space of semiconductor qubit devices and circuits and for their parameterization in higher-level qubit circuit models. We will provide the community with the needed state-of-the art Nanoelectronic Modeling tool. </p>
		
		
		
		
		
