		<h2>Gerhard Klimeck J_2001_5  </h2>
				
		<h3>&quot;Strong wavevector dependence of hole transport in heterostructures&quot;,		</h3>
		<p>Gerhard Klimeck, R. Chris Bowen, and Timothy B. Boykin,
		<p>Superlattices and Microstructures, Vol. 29, No. 3, pg. 187-216 (2001).	
		<h3>Abstract:</h3>: <p>Heterostructures such as resonant tunneling diodes, quantum well photodetectors and lasers, and cascade lasers break the symmetry of the crystalline lattice. Such break in lattice symmetry causes a strong interaction of heavy-, light- and split-off hole bands. A resonant tunneling diode is used as a vehicle to study hole transport in heterostructures including the subband dispersion transverse to the main transport direction. Four key findings are demonstrated: (1) the heavy and light hole interaction is shown to be strong enough to result in dominant current flow off the zone center (more holes flow through the structure at an angle than straight through), (2) explicit inclusion of the transverse momentum in the current integration is needed, (3) most of the current flow is due to injection from heavy holes in the emitter, and (4) the dependence on the angle Phi of the transverse momentum k is weak. Two bandstructure models are utilized to demonstrate the underlying physics: (1) independent/uncoupled heavy-, light- and split-off bands, and (2) second-nearest neigh-bor sp3s* tight-binding model. Current&#150;voltage (I &#150;V) simulations including explicit inte-gration of the total energy E, transverse momentum |k |and transverse momentum angle Phi are analyzed. An analytic formula for the current density J(k) as a function of transverse momentum k is derived and utilized to explain the three independent mechanisms that generate off zone center current flow: (1) nonmonotonic (electron-like) hole dispersion, (2) different quantum well and emitter effective masses, and (3) momentum dependent quantum well coupling strength. The analytic expression is also used to generate a com-plete I &#150;V characteristic that compares well to the full numerical solution. The Fermi level and temperature dependence on the I &#150;V is examined. Finally a simulation is compared to experimental data.</p>
		
