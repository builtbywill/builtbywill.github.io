		<h2>Gerhard Klimeck C_2002_14 </h2>
				
		
		<h3>Image Correlation Quality Control enabled by Cluster Computing</h3>
		
		<p>Gerhard Klimeck, Gary Yagi, Robert Deen, Myche McAuley, Eric DeJong, Fabiano Oyafuso,</p>
		
		<p>2nd JPL IT Symposium, Pasadena, CA, Nov 4, 2002.</p>
		
		<h3>Abstract:</h3> 
        <p>Stereo image correlation is an essential ingredient to mars missions for mapping and ranging purposes.  Parallelization of available correlation software reduced the required processing time from about 60 to 90 minutes to 2-3 minutes per image pair.  A quality control of the correlation has been previously too time intensive, but is now enabled by the fast parallel execution. The stereo image correlation software attempts to deliver for every pixel (x,y) in the reference image (left or right) a corresponding pixel coordinate (x',y') in the corresponding pair (right or left).  This is performed in a parallel algorithm that subdivides the reference image onto different CPUs. The important thing to realize is that the quality of the mapping from left image pixels to right image pixels may not be perfect due to several reasons. Two of the major reasons are: 1) the left eye might see objects that the right eye does not, and vice versa (parallax), and 2) the image data might be noisy.  The correlation / mapping output delivered by the correlation software may therefore be faulty. The quality of the correlation is assessed by checking a left-&gt;right correlation by a right-&gt;left correlation.  It is based on establishing TWO correlation maps left-&gt;right and right-&gt;left,  therefore doubling the overall workload.  The originally desired mapping relates the point (x,y) to the point (x',y'). The corresponding mapping from (x',y') back to the left will result in the pixel (x'',y''). If (x,y) and (x'',y'') are identical we consider this mapping a perfect correlation.  The very hard criterion of a perfect match can be relaxed with the introduction of an error window, which accounts for integer interpolation round-off and a small amount of noise.  The procedure can be repeated until all unconnected point-to-point connections are eliminated, resulting in verified correlation data.</p>
		
		
		
