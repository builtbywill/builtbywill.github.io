		<h3>&quot;The Theory of Single and Multi-Band Modeling of Wide Cross-Sectional Area, Steady-State, High Bias Quantum Devices&quot;,</h3>
		
		<p>Roger Lake, Gerhard Klimeck, R. Chris Bowen and Dejan Jovanovic</p>
		
		<p>J. of Appl. Phys. 81, 7845 (1996).</p>
		
		<h3>Abstract:</h3>
        <p>Non-equilibrium Green function theory is formulated to meet the three main challenges of high bias quantum device modeling: self-consistent charging, incoherent and inelastic scattering, and band structure. The theory is written in a general localized orbital basis using the example of the zinc blende lattice. A Dyson equation treatment of the open system boundaries results in a tunneling formula with a generalized Fisher-Lee form for the transmission coefficient that treats injection from emitter continuum states and emitter quasi-bound states on an equal footing. Scattering is then included. Self-energies which include the effects of polar optical phonons, acoustic phonons, alloy fluctuations, interface roughness, and ionized dopants are derived. Interface roughness is modeled as a layer of alloy in which the cations of a given type cluster into islands. Two different treatments of scattering; self-consistent Born and multiple sequential scattering are formulated, described, and analyzed for numerical tractability. The relationship between the self-consistent Born and multiple sequential scattering algorithms is described, and the convergence properties of the multiple sequential scattering algorithm are numerically demonstrated by comparing with self-consistent Born calculations. (C) 1997 American Institute of Physics.</p>
		
