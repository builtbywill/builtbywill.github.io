		<h2>Gerhard Klimeck C_2004_2 </h2>
				
		
		<h3>Effect of strain on the phonon dispersion in Ge/Si quantum dot superlattices with an atomistic level model</h3>
		
		<p>Olga L. Lazarenkova, Paul von Allmen, Fabiano Oyafuso, Seungwon Lee, Gerhard Klimeck</p>
		March Meeting of the American Physical Society, March 22-26, Montreal, CA (2004).</p>
		<h3>Abstract:</h3> 
		
		<p>Strain in nanostructures such as quantum dot superlattices (QDSL) affects the phonon dispersion and therefore the lattice contribution to the thermal conductivity and the thermoelectric figure of merit. Previous efforts have relied on continuum models or on the atomistic level Keating valence force field model to compute the phonon dispersion. However, for Ge/Si QDSL, continuum models do not adequately describe the formation of stop-bands, which have been predicted to reduce the thermal conductivity when compared to the bulk material. The Keating model, on the other hand, does not reproduce the correct effect of deformation on phonon spectrum, which leads to an overestimation of the stop-band width. We have developed an extension to the Keating model with terms in the inter-atomic potential beyond the harmonic approximation. This new model predicts a reduction in strain for a Ge/Si QDSL structure and more accurately describes the effect of strain on the phonon dispersion, in particular the formation of stop-bands. This work was performed while one of the authors (O.L.L.) held a National Research Concil Research Associateship Award at the Jet Propulsion Laboratory.</p>		
		
		
		
