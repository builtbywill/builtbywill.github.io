		<h3>Nanoelectronic Device Calculations from an Atomistic Point of View:
		Empirical Tight-Binding Models for Semiconductor Heterostructures</h3>
		
		<p>Timothy B. Boykin, Gerhard Klimeck, and R. Chris Bowen,</p>
		
		<p>Nanospace 2000, Houston, TX Jan. 23-28, 2000.</p>
		
		<p>Models of classical electronic devices, such as the silicon MOSFET, are grounded upon the assumption that all parts of the device are sufficiently large that the transport and optical properties are essentially bulk-like, the carriers being described by essentially free- particle semi-classical dynamics. Modern growth techniques such as MBE and MOCVD can reliably produce layers only tens of Angstroms (a few atomic planes) thick, so that bulk-like descriptions are no longer valid, and a more complete description is required for nanostructures and nanodevices. The Empirical Tight-Binding Method (ETBM) offers such a more complete description, for it incorporates the symmetries of the crystals from which the devices are made and thus automatically takes into account bandstructure effects throughout the Brillouin Zone: effects of satellite valleys and even other bands are therefore built into the calculation from the start as opposed to being added later. Based upon atomic-like orbitals, the ETBM offers a straightforward treatment of the heterointerfaces present in nanostructures and is well-suited for calculations of both transport and optical properties. We discuss the properties of tight-binding models, factors involved in choosing the best tight-binding model for a given problem, and some interesting and surprising results of tight-binding calculations for heterostructures.</p>
		
		
		
