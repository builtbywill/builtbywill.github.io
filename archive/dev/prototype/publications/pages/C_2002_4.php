		<h3>Many-body interactions in multiply-charged quantum dots modeled by empirical tight-binding</h3>
		
		<p>Seungwon Lee, Lars J&ouml;nsson, Jeongnim Kim, John W. Wilkins, Garnett W. Bryant, and Gerhard Klimeck</p>
		<p>March Meeting of the American Physical Society, March 17-22, Indianapolis, IN (2002).		</p>
		<h3>Abstract:</h3> 
        <p>Recent STM measurements successfully resolve the richness and complexity of many-body interactions in multiply-charged single quantum dots. Many-body interactions including both direct Coulomb and exchange interactions are calculated with atomistic tight-binding wave functions for InAs spherical quantum dots. In negatively charged quantum dots, sparse electron levels and, consequently, weak Coulomb coupling make it reasonable to treat the many-body interactions as a perturbation. The perturbation treatment successfully identifies the energy spectra of the negatively-charged quantum dots. This agreement verifies our empirical tight-binding model for the electron levels and many-body interactions. In contrast to the simple spectra of the negatively-charged dots, the strong Coulomb coupling between dense and multi-degenerate hole levels leads to complex spectra in positively charged quantum dots. Significant correlation effects in the positively-charged dots are captured by treating many-body interactions with configuration interactions. </p>
		
				
		
