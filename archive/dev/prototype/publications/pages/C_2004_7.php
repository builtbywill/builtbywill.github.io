		<h2>Gerhard Klimeck C_2004_7 </h2>
				
		
		<h3>Conduction band valley splitting in Si</h3>
		
		<p>Gerhard Klimeck, T. B. Boykin, M. Eriksson, M. Friesen, S. N. Coppersmith, P. von Allmen, F. Oyafuso, S. Lee</p>
		<p>March Meeting of the American Physical Society, March 22-26, Montreal, CA (2004).</p>
		<h3>Abstract:</h3> 
		
		<p>A theory based on localized-orbital approaches is developed to describe the valley splitting observed in silicon nano-structures. The theory is appropriate in the limit of low electron density and relevant for proposed quantum computing architectures. The valley splitting is computed for realistic devices using the quantitative nanoelectronic modeling tool NEMO using the empirical tight binding model sp^3d^5s. The tight binding parameters have been fitted to bulk bandstructure behavior of Si using a genetic algorithm. A 1-D quantum well simulation in NEMO shows the basic features of conduction band valley splitting as a coherent, confinement-induced phenomenon. No additional intervalley scattering parameters are needed. The splitting is in general nonzero even in the absence of electric field. The splitting oscillates as a function of N, the number of layers in the quantum well, with a period that is determined by the location of the valley minimum in the Brillouin zone. The envelope of the splitting decays as N^3. The qualitative physics remain the same irrespective of the details of the quantum well boundaries or the details of the strain treatment in the quantum well.</p>
		
		
		
		
