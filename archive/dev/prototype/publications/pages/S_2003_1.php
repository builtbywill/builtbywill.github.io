		<h2>Gerhard Klimeck S_2003_1  </h2>
				
		
		<h3>Computational Nanoelectronics Towards: Design, Analysis, Synthesis, and Fundamental Limits</h3>
		
		
		
		<p>Gerhard Klimeck, </p>
		
		<p>Dr. Ed Stone Award Outstanding Research Publication Award Lecture, JPL, Feb. 18, 2003.</p>
		
		<p>Computational Nanoelectronics Towards: Design, Analysis, Synthesis, and Fundamental Limits</p>
		
		<p>Nanotechnology and Nanoelectronics in particular have received a lot of public attention since President Clinton announced a National Nanotechnology Initiative at Caltech.  Nanotechnology bears the promise to provide new approaches to electronics, materials, medicine, biology and a variety of other technologies.  In the world of semiconductor electronics nanotechnology has already arrived in practical devices.  Wave-like properties of electrons modify the functional device behavior once spatial variations in structures reach length scales of a few tens of nanometers.  While standard technologies require extensive simulation tools for design, analysis and characterization, no such tools exist for nanoelectronic devices. The modeling and simulation of such devices now deviates strongly from classical approaches: it must be fundamentally quantum mechanical.  JPL has been involved in nanoelectronic modeling since 1998 following efforts started at Texas Instrument in 1993.  This seminar will review the development of a comprehensive nanoelectronic modeling tool (NEMO 1-D and NEMO 3-D) and its application to high-speed electronics (resonant tunneling diodes) and IR detectors and lasers (quantum dots and 1-D heterostructures).</p>
		
		
		<p>In the year of 2002 Gerhard Klimeck was one of the four winners of the JPL Dr. Stone award for outstanding research publications with the following citation: &#147;This paper represents a milestone publication for JPL&#146;s entry into the field of nanoelectronic device modeling and simulation for future NASA missions. It is also of value to the international semiconductor industry community.&#148;</p>
		<p>Dr. Gerhard Klimeck is the technical group supervisor of the Applied Cluster Computing Technologies Group and a Principal member at the NASA Jet Propulsion Laboratory. He joined JPL in February 1998 to engage in research in electron transport through nanoelectronic devices, parallel cluster computing, genetic algorithms, and parallel image processing. At JPL he has utilized technology in these areas to explore the nanoelectronic design space and has developed the 3-D Nanoelectronic Modeling tool (NEMO 3-D) that enables the analysis of electronic structure in systems containing as many as 32 million atoms. His work on mars image processing enabled near real-time data processing.  Previously he was a member of technical staff at the Central Research Lab of Texas Instruments (which transitioned to the Applied Research Laboratory of Raytheon) where he served as manager and principal architect of the Nanoelectronic Modeling (NEMO) program. Dr. Klimeck received his Ph.D. in 1994 from Purdue University and Dipl. Ing from Ruhr-University Bochum, Germany. Dr. Klimeck's work is documented in over 75 peer reviewed publications and over 120 conference presentations. Other authors have cited these publications over 300 times.  He is a member of IEEE, APS, HKN and TBP.</p>
