		<h3>&quot;Diagonal parameter shifts due to nearest-neighbor displacements in empirical tight-binding theory&quot;,</h3>
			
		<p>Timothy B. Boykin, Gerhard Klimeck, R. Chris Bowen, and, Fabiano Oyafuso</p>
			
		<p>Phys. Rev. B 66, 125207 (2002).</p>
		
		
		
		<h3>Abstract:</h3>
		<p>Nano-scale heterostructures are generally characterized by local strain variations within both the structures and their immediate environment.  Because the atoms in such systems can be irregularly positioned, theroretical models and parameterizations that are restricted to hydrostatic and uniaxial strain are generally not applicable.  To address this shortcoming, we present a method that enables the incorporation of general distortions into the empirical tight binding model. The method shifts the diagonal Hamiltonian matrix elements due to displacements of neighboring atoms from their ideal bulk positions.  The new, efficient, and flexible method is developed for zincblende semiconductors and employed to calculate gaps for GaAs and InAs under hydrostatic and uniaxial strain. Our new method, which is not restricted to the cases of uniaxial or hydrstatic strain, compares favorably with other methods for which experimental and theoretical data are available.  Because our method handles arbitrary nearest-neighbor displacements it permits the incorporation of diagonal parameter shifts in general nano-scale electronic structure simulations.
		
		
