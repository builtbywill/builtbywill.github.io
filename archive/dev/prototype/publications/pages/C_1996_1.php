		<h3>NanoElectronic MOdeling (NEMO)</h3>
		
		<p>D. Blanks, G. Klimeck, R. Lake, R. Bowen, T. Moise, D. Jovanovic (Texas Instruments Inc., Dallas, TX), M. Leng, W. Frensley (U. Texas at Dallas), P. Sotirelis (U. Illinois),</p>
		
		<p>March meeting of the American Physical Society, Saint Louis, March, 1996		</p>
		
		<h3>Abstract:</h3> 
		<p>We present a new modeling program that calculates the electronic properties of one-dimensional nanoelectronic resonant tunneling devices. The NanoElectronic MOdeling (NEMO) program uses a non-equilibrium Green function formalism to determine current, energy resonances, band energy profile, and charge density as a function of bias and temperature. Quantum charge, resonant levels, scattering, and band structure effects are taken into account. The scattering mechanisms include polar-optical phonon, acoustic phonon, alloy, and interface roughness scattering. Novel boundary conditions treat large leads with spatially varying potentials and injection into the device from quantized emitter states. Calculation results were compared to current vs voltage measurements performed on double-barrier resonant tunneling diodes with barrier-well combinations of AlGaAs-GaAs, AlAs-GaAs, and InAlAs-InGaAs. Comparisons between theory and experiment will be presented for a variety of calculation options. </p>
		
		
