		<h3>Applications on High Performance Cluster Computers</h3>

<p>Production of Mars Panoramic Mosaic Images</p>


		
		
		<p>Tom Cwik, Gerhard Klimeck, Myche McAuley, Bob Deen and Eric Dejong </p>
		
		
		
		<p>2001 <a href="http://ulua.mhpcc.af.mil/AMOS/conf_01.html">AMOS Technical Conference</a>, September 10-14, Maui</p>

		
		<h3>Abstract:</h3> <p>The development, application and commercialization of cluster computer systems have escalated dramatically over the last several years. Driven by a range of applications that need relatively low-cost access to high performance computing systems, cluster computers have reached worldwide acceptance and use. A cluster system consists of commercial-off-the-shelf hardware coupled to (generally) open source software. These commodity personal computers are interconnected through commodity network switches and protocols to produce scalable computing systems usable in a wide range of applications. </p>
		<p>
			One application currently under development to utilize cluster computer resources is the production of Martian surface mosaic images from a collection of individual images shot from a rover camera. The mosaic production software was ported to a commodity cluster computer system as a proof-of-concept for near real-time processing of the downlink data. This talk will describe the requirements for parallelization of the algorithms as well as performance improvements using the cluster computer that enable its use in real-time mission operations.</p>
		
		
		
		
