		<h3>&quot;Electron-hole correlations in semiconductor quantum dots with tight-binding wave functions&quot;,</h3>
		
		<p>Seungwon Lee, Lars J&ouml;nsson, and John W. Wilkins, Garnett Bryant, and Gerhard Klimeck,	</p>
		<p>Phys. Rev. B Vol. 63, 195318 (2001).</p>
		
		<h3>Abstract:</h3>: <p>The electron-hole states of semiconductor quantum dots are investigated within the framework of empirical tight-binding descriptions for Si, as an example of an indirect gap material, and InAs and CdSe as examples of typical III-V and II-VI direct-gap materials. The electron-hole Coulomb interaction is largely insensitive to both the real-space description of the atomic basis orbitals and different ways of optimizing the tight-binding parameters. Tight-binding parameters that are optimized to give the best effective masses significantly improve the energies of the excitonic states due to the better single-particle energies. However, the Coulomb interaction does not vary much between different parameterizations. In addition, the sensitivity of the Coulomb interaction to the choice of atomic orbitals decreases with increasing dot size. Quantitatively, tight-binding treatments of correlation effects are reliable for dots with radii larger than 15-20A. The calculated excitonic gaps are in good agreement with recent photoluminescence data for Si and CdSe but agree less well for InAs. Further, the effective range of the electron-hole exchange interaction is investigated in detail. In quantum dots of the direct-gap materials InAs and CdSe, the exchange interaction can be long-ranged, extending over the whole dot when there is no local (onsite) orthogonality between the electron and hole wave functions. By contrast, for Si quantum dots the extra phase factor due to the indirect gap effectively limits the range to about 15A, independent of the dot size</p>
